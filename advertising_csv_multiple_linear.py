import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.linear_model import LinearRegression
import statsmodels.api as sm

df=pd.read_csv("/content/advertising.csv")

df.head()

df.duplicated().sum()

df.info()

df.describe(include='all')

df.isnull().sum()

X = df[['TV', 'Radio', 'Newspaper']]
y = df['Sales']

model = LinearRegression()
model.fit(X, y)

print("\nIntercept:", model.intercept_)
print("Coefficients:", model.coef_)

# Step 7: Match coefficients with features
coef_df = pd.DataFrame({
    'Feature': X.columns,
    'Coefficient': model.coef_
})
print("\nCoefficient Table:\n", coef_df)

# Step 8: Evaluate using R²
print("\nR² Score:", model.score(X, y))

X_sm = sm.add_constant(X)  # Add intercept term
ols_model = sm.OLS(y, X_sm).fit()
print("\nOLS Regression Results:\n", ols_model.summary())